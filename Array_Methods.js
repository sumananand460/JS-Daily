/**
 * JAVASCRIPT ARRAY METHODS SUMMARY
 * Use these to manage, change, or search through lists of data.
 */

// --- ADDING & REMOVING ---

// .push()    -> **Adds** an element to the **end** of the array.
// .pop()     -> **Removes** the **last** element from the array.
// .unshift() -> **Adds** an element to the **front** of the array.
// .shift()   -> **Removes** the **first** element from the array.
// .splice()  -> **Adds or removes** elements at a specific index.

// --- TRANSFORMING (CREATING NEW ARRAYS) ---

// .map()     -> **Creates a new array** by running a function on every item.
// .filter()  -> **Creates a new array** containing only items that pass a test.
// .slice()   -> **Copies** a specific portion of an array into a new one.
// .concat()  -> **Joins** two or more arrays together.
// .flat()    -> **Flattens** nested arrays (arrays inside arrays) into one.

// --- SEARCHING & TESTING ---

// .find()      -> Returns the **first item** that matches a condition.
// .findIndex() -> Returns the **position (index)** of the first item found.
// .includes()  -> Returns **true** if the array contains a specific value.
// .indexOf()   -> Returns the **first index** where a value is found.
// .every()     -> Returns **true** if **all items** pass a specific test.
// .some()      -> Returns **true** if **at least one item** passes a test.

// --- ORDERING & CALCULATING ---

// .sort()    -> **Rearranges** items (default is alphabetical/ascending).
// .reverse() -> **Flips** the order of the array.
// .reduce()  -> **Condenses** the whole array into a **single value** (like a total sum).
// .join()    -> **Combines** all items into a single string.

// --- LOOPING ---

// .forEach() -> **Runs a function** for every single item (does not return a new array).