/**
 * JAVASCRIPT ARRAY AND STRING METHODS QUICK REFERENCE
 * * I've compiled the most common methods used in daily coding. 
 * They are organized by what they do to keep things simple.
 */

// --- ARRAY METHODS: ADDING/REMOVING ---

// .push()    -> **Adds** an item to the **end** of the array.
// .pop()     -> **Removes** the **last** item from the array.
// .unshift() -> **Adds** an item to the **beginning**.
// .shift()   -> **Removes** the **first** item.
// .splice()  -> **Adds or removes** items at a specific index.

// --- ARRAY METHODS: TRANSFORMING ---

// .map()     -> **Creates a new array** by doing something to every item.
// .filter()  -> **Creates a new array** with only items that pass a test.
// .reduce()  -> **Shrinks** the whole array down to a **single value** (like a sum).
// .sort()    -> **Rearranges** the items in order (alphabetical or numeric).
// .reverse() -> **Flips** the order of the array.

// --- ARRAY METHODS: SEARCHING ---

// .find()      -> Returns the **first item** that matches a condition.
// .findIndex() -> Returns the **position (index)** of the first item found.
// .includes()  -> Returns **true or false** if an item exists in the array.
// .indexOf()   -> Returns the **index** of a specific value.

// --- STRING METHODS ---

// .toUpperCase() -> Converts the whole string to **capital letters**.
// .toLowerCase() -> Converts the whole string to **small letters**.
// .trim()        -> **Removes extra spaces** from both ends of the string.
// .split()       -> **Breaks a string** into an array of smaller pieces.
// .replace()     -> **Swaps** a specific word or character for a new one.
// .slice()       -> **Cuts out** a piece of the string from a start to end point.

// --- OBJECT METHODS ---

// Object.keys()   -> Returns an array of all **property names**.
// Object.values() -> Returns an array of all **property values**.
// Object.assign() -> **Copies** properties from one object to another.